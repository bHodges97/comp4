package math;

/**
 * The MyVector class represents a 2d vector.
 * 
 */
public class MyVector {
	public Double x;
	public Double y;

	/**
	 * Constructs a new vector with parameters x and y
	 * 
	 * @param x
	 *            The x value of the vector
	 * @param y
	 *            The y value of the vector
	 */
	public MyVector(Double x, Double y) {
		this.x = x;
		this.y = y;
	}
}
